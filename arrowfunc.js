// arrow function with basic syntax.
let func1 = () => {
    let str = "Hello Reyna Welcome";
    console.log(str);
}
func1(); 


// arrow function with  return keyword.
let func2 = () => {
    return Name = "Hello Reyna I am waiting for Omen";
}
console.log(func2());


//  arrow function with no parentheses.
let func3 = () => "Hello Reyna Again!!"
console.log(func3());


// arrow function with parameters passes as an argument.
let func4 = (val) => "Hello you've come again " + val;
console.log(func4("Reyna"));


//  arrow function with parameters passes as an argument without parentheses.
let func5 = val => "Hello! Finally you've come " + val;
console.log(func5("Omen!!"));
